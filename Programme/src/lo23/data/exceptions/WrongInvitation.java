
package lo23.data.exceptions;

/**
 * Thrown in createGame when an invitation doesn't contain the currentProfile
 * @author ksadorf
 */

public class WrongInvitation extends Exception {
    /**
     * Default constructor
     */
    public WrongInvitation()
    {
        super();
    }

    /**
     * Constructor, with a given message
     * @param msg The message contained into the thrown exception
     */
    public WrongInvitation(String msg)
    {
        super(msg);
    }
}
